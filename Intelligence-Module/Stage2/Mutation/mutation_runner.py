"""
Mutation Runner for Stage-2 (Layer 3)

Runs the validated test suite from Stage 1 against each mutant
generated by mutation_engine.

For each test × mutant pair, determines:
    - killed:   test passes on original, fails on mutant → test is strong
    - survived: test passes on both → test didn't detect the change
    - ignored:  test was already failing on original → not informative

Produces per-test kill counts and per-mutant survival status.
Tests that kill zero mutants are flagged as weak oracles.

Integration point:
    Called by signal_filter.py after mutation_engine produces mutants.
    Results feed into report_builder for final classification.

Current increment:
    - Reuses Stage 1's test_executor.run_tests for execution
    - Python only
    - Single-process sequential execution

Future increment placeholders:
    - Parallel mutant execution
    - Early termination (stop testing a mutant once it's killed)
    - Mutant result caching across pipeline runs
"""

from Stage1.Tools.test_executor import run_tests


class Mutation_Runner:
    def __init__(self):
        pass

    def run_against_mutants(self, mutants, tests, execution_model, original_results):
        """
        Runs test suite against each mutant and compares to original results.

        Args:
            mutants: list of mutant dicts from mutation_engine
                     each has: id, operator, line, description, source_code
            tests: list of test dicts from Stage 1 (the validated test suite)
            execution_model: callable_method / stdin_program / script
            original_results: list of result dicts from Stage 1's test_executor
                              (the results of running tests on original code)

        Returns:
            {
                "test_scores": {test_index: {"kill_count": int, "killed_mutants": [ids]}},
                "mutant_results": {mutant_id: {"status": killed/survived, "killed_by": [test_indices]}},
                "summary": {"total_mutants": int, "killed": int, "survived": int, "mutation_score": float}
            }
        """
        test_scores = {}
        mutant_results = {}

        # initialize test scores
        for i in range(len(tests)):
            test_scores[i] = {
                "kill_count": 0,
                "killed_mutants": []
            }

        print(f"    [Mutation Runner] Running {len(tests)} tests against {len(mutants)} mutants")

        for mutant in mutants:
            mutant_id = mutant["id"]
            mutant_code = mutant["source_code"]

            mutant_results[mutant_id] = {
                "status": "survived",
                "killed_by": [],
                "operator": mutant["operator"],
                "line": mutant["line"],
                "description": mutant["description"]
            }

            # run all tests against this mutant
            try:
                mutant_exec_results, _ = run_tests(mutant_code, tests, execution_model)
            except Exception as e:
                # mutant crashed during setup — counts as killed by all tests
                print(f"    [Mutation Runner] Mutant {mutant_id} crashed: {e}")
                mutant_results[mutant_id]["status"] = "killed"
                continue

            # compare each test's result on mutant vs original
            for test_idx, mutant_result in enumerate(mutant_exec_results):
                if test_idx >= len(original_results):
                    continue

                original_result = original_results[test_idx]
                kill_status = self.check_kill(
                    original_result, mutant_result, tests[test_idx]
                )

                if kill_status == "killed":
                    test_scores[test_idx]["kill_count"] += 1
                    test_scores[test_idx]["killed_mutants"].append(mutant_id)

                    mutant_results[mutant_id]["status"] = "killed"
                    mutant_results[mutant_id]["killed_by"].append(test_idx)

            status = mutant_results[mutant_id]["status"]
            print(
                f"    [Mutation Runner] Mutant {mutant_id} "
                f"({mutant['description']}): {status}"
            )

        # build summary
        total = len(mutants)
        killed = sum(1 for m in mutant_results.values() if m["status"] == "killed")
        survived = total - killed
        mutation_score = killed / total if total > 0 else 0.0

        summary = {
            "total_mutants": total,
            "killed": killed,
            "survived": survived,
            "mutation_score": mutation_score
        }

        print(
            f"    [Mutation Runner] Mutation score: "
            f"{killed}/{total} = {mutation_score:.2%}"
        )

        return {
            "test_scores": test_scores,
            "mutant_results": mutant_results,
            "summary": summary
        }

    def check_kill(self, original_result, mutant_result, test):
        """
        Determines if a test killed a mutant.

        Kill condition:
            Test PASSES on original code AND FAILS on mutant.
            "Fails" means: different output, exception, timeout, or crash.

        Returns:
            "killed"  — test detected the mutation
            "survived" — test did not detect the mutation
            "ignored"  — test was already failing on original (not informative)
        """
        original_status = original_result.get("status")
        mutant_status = mutant_result.get("status")

        # test already failing on original — can't tell us anything
        if original_status != "success":
            return "ignored"

        # test fails on mutant — killed
        if mutant_status != "success":
            return "killed"

        # both succeeded — compare outputs
        original_output = original_result.get("output")
        mutant_output = mutant_result.get("output")

        if original_output != mutant_output:
            return "killed"

        # same output on both — mutant survived
        return "survived"

